// ── Helper functions ──────────────────────────────────────────────────────────

export function getMentorById(mentorId) {
  return MENTORS.find((m) => m.id === mentorId);
}

/** YouTube sources derived from mock mentors — used when Base44 Source entity is off */
export function buildSyntheticSourcesFromMentors(mentors) {
  if (!Array.isArray(mentors)) return [];
  return mentors
    .map((m) => {
      if (m.youtubeChannelId) {
        return {
          id: `src_${m.id}`,
          mentorId: m.id,
          sourceType: "youtube",
          sourceUrl: `https://www.youtube.com/channel/${m.youtubeChannelId}`,
          active: m.active !== false,
          lastCheckedAt: null,
        };
      }
      const page = String(m.youtubePageUrl || m.channelUrl || m.youtubeUrl || "").trim();
      if (!page) return null;
      const lower = page.toLowerCase();
      const sourceType =
        lower.includes("youtube.com") || lower.includes("youtu.be")
          ? "youtube"
          : lower.includes("rss") || lower.includes("feed") || lower.endsWith(".xml")
            ? "rss"
            : "site";
      return {
        id: `src_${m.id}`,
        mentorId: m.id,
        sourceType,
        sourceUrl: page,
        active: m.active !== false,
        lastCheckedAt: null,
      };
    })
    .filter(Boolean);
}

export function getVideosByStatus(status) {
  return VIDEOS.filter((v) => v.status === status);
}

export function getVideosByMentor(mentorId) {
  return VIDEOS.filter((v) => v.mentorId === mentorId);
}

export function getVideosByTopic(topicId) {
  return VIDEOS.filter((v) => v.topicIds?.includes(topicId));
}

export function getKpiStats() {
  const today = new Date().toISOString().split("T")[0];
  return {
    newToday: VIDEOS.filter(
      (v) => v.status === "new" && v.publishedAt.startsWith(today)
    ).length,
    totalNew: VIDEOS.filter((v) => v.status === "new").length,
    summarized: VIDEOS.filter((v) => v.status === "done").length,
    processing: VIDEOS.filter((v) => v.status === "processing").length,
    errors: VIDEOS.filter((v) => v.status === "error").length,
  };
}
