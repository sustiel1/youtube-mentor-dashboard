// ── Helper functions ──────────────────────────────────────────────────────────

export function getMentorById(mentorId) {
  return MENTORS.find((m) => m.id === mentorId);
}

export function getVideosByStatus(status) {
  return VIDEOS.filter((v) => v.status === status);
}

export function getVideosByMentor(mentorId) {
  return VIDEOS.filter((v) => v.mentorId === mentorId);
}

export function getVideosByTopic(topicId) {
  return VIDEOS.filter((v) => v.topicIds?.includes(topicId));
}

export function getKpiStats() {
  const today = new Date().toISOString().split("T")[0];
  return {
    newToday: VIDEOS.filter(
      (v) => v.status === "new" && v.publishedAt.startsWith(today)
    ).length,
    totalNew: VIDEOS.filter((v) => v.status === "new").length,
    summarized: VIDEOS.filter((v) => v.status === "done").length,
    processing: VIDEOS.filter((v) => v.status === "processing").length,
    errors: VIDEOS.filter((v) => v.status === "error").length,
  };
}
