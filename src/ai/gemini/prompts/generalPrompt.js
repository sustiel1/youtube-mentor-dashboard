/**
 * Builds the analysis prompt for general/educational content.
 * Extracted from vite.config.js buildClaudeStyleGeminiAnalysisLines.
 */
export function buildGeneralAnalysisPrompt({
  chaptersTarget,
  title,
  mentor,
  category,
  durationSeconds,
  chapterHintsText,
  transcriptMode = 'full',
}) {
  const chapterInstruction =
    transcriptMode === 'short'
      ? 'התמלול קצר — אל תמציא פרקים שאינם מגובים בטקסט. אם אין מספיק תוכן לפרקים, החזר chapters: [].'
      : transcriptMode === 'medium'
      ? 'צור 2-3 פרקים אם הנושאים ברורים מהתמלול. אחרת, העדף סיכום איכותי על פני פרקים בינוניים.'
      : `לסרטון הזה צור בערך ${chaptersTarget} פרקים איכותיים, כך שהפרקים יכסו את כל הסרטון.`;

  const modeNote =
    transcriptMode === 'short'
      ? 'התמלול קצר — נתח את התוכן האמיתי שבתמלול בלבד. אם התמלול קצר — תן ניתוח קצר אבל אמיתי. אל תכתוב ביטויים כמו "מדומה", "לדוגמה", "placeholder", "תובנה מהתוכן" — רק תוכן שמופיע ממש בטקסט.'
      : transcriptMode === 'medium'
      ? 'התמלול בינוני — ספק סיכום מלא ונקודות מפתח מהתוכן האמיתי. אל תכתוב ערכים גנריים או ביטויים כמו "מדומה", "לדוגמה", "placeholder". פרקים רק אם הנושאים ברורים.'
      : null;

  return [
    'נתח את התמלול הבא בלבד והחזר JSON בלבד, בלי markdown ובלי טקסט נוסף.',
    '',
    '═══ מטרת הניתוח ═══',
    'המטרה אינה לסכם מה קרה בסרטון, אלא לחלץ ידע אישי מקצועי שניתן לעשות בו שימוש חוזר.',
    'שאל: "אם שכחתי לגמרי את הסרטון — מה הייתי רוצה שיישאר?" — זה הידע שצריך לחלץ.',
    '',
    '═══ כללי חילוץ ידע ═══',
    'כל פריט ידע חייב לבטא רעיון אחד ויחיד — אם יש כמה רעיונות, פצל לפריטים נפרדים.',
    'כל פריט חייב להיות reusable גם מחוץ לסרטון הספציפי — ללא הקשר.',
    'העדף ניסוח: עיקרון, כלל, תובנה, שאלה, ממצא — לא סיפור.',
    'אל תכתוב recap, filler, או משפטים שמתחילים ב"הסרטון מסביר" / "המרצה אומר".',
    'אם יש דעה — נסח אותה כעיקרון בדיק, שאלה מעצבנת, או מסגרת חשיבה.',
    'אחד פריטים דומים — אין כפילויות, אין near-duplicates.',
    '',
    '═══ כללי פרקים ═══',
    'פרקים = גבולות סמנטיים אמיתיים לפי נושאים, לא חלוקת זמן שווה.',
    'אסור לפתוח פרק שלא קיים בתמלול — כל פרק חייב להתבסס על תוכן אמיתי.',
    'כל כותרת פרק חייבת לכלול לפחות אחד מ: שם ספציפי, מספר/אחוז, מונח טכני מהתמלול.',
    'כותרות תקינות: "S&P -1.5%, VIX מזנק" | "NVDA: פריצת resistance" | "פאול: הורדת ריבית ב-2025?".',
    'כותרות אסורות: "פתיח", "סיכום", "מבוא", "פרק 1", "ניתוח כללי", "נקודות מפתח", "מבט קדימה".',
    chapterInstruction,
    '',
    '═══ כללי איכות ═══',
    'אסור בהחלט: "מדומה", "placeholder", "example", "תובנה מהתוכן", ביטויים גנריים שמתאימים לכל סרטון.',
    'אם אין חומר חזק לשדה מסוים — החזר מערך ריק, לא ערך גנרי.',
    'אם אין timestamps — הערך זמנים לפי התקדמות הטקסט.',
    modeNote,
    'עברית תקינה, ברורה ומעשית.',
    '',
    `כותרת: ${title}`,
    mentor ? `מנטור: ${mentor}` : null,
    category ? `קטגוריה: ${category}` : null,
    Number.isFinite(Number(durationSeconds)) && Number(durationSeconds) > 0
      ? `משך סרטון בשניות: ${Math.floor(Number(durationSeconds))}`
      : null,
    '',
    'סוגי הידע הנדרשים: Insight, Rule, Checklist, Warning, Framework, Concept, Thesis, Question, Action.',
    'החזר רק את הפריטים הטובים ביותר בכל קטגוריה. עדיף מעט פריטים חזקים על הרבה פריטים חלשים.',
    'אם אין מספיק חומר טוב בקטגוריה, החזר מערך ריק.',
    '',
    'פרקים: גבולות סמנטיים אמיתיים לפי נושאים בתמלול; השתמש בחותמות הזמן מהתמלול או ממקטעי הטקסט כשקיימים.',
    'אל תיצור פרקי placeholder או כותרות מסוג "פרק 1/2/3".',
    '',
    'רמזי פרקים/YouTube (אם קיימים):',
    chapterHintsText,
  ].filter(Boolean).join('\n');
}
